int	ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int flag;

	flag = 0;
	while (flag < (length - 1))
	{
		if (f(tab[flag], tab[flag + 1]) > 0)
			return (0);
		flag++;
	}
	return (1);
}
